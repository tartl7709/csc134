
// handling arithmetic for dataman calculator


int add (int num1, int num2)
{
    // take two numbers, find the sum, print the result
    int sum = num1 + num2;
    return sum;
}
int subtract (int num1, int num2)
{
    int difference = num1 - num2;
    return difference;
}
float divide (float num1, float num2)
{
    float dividend = num1 / num2;
    return dividend;
}
int multiply (int num1, int num2)
{
    int mult = num1 * num2;
    return mult;
}
